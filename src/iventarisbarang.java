    private void btnExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportActionPerformed
  // Pilih lokasi untuk menyimpan file CSV
    JFileChooser fileChooser = new JFileChooser();
    fileChooser.setDialogTitle("Simpan sebagai CSV");
    
    // Hanya izinkan ekstensi .csv
    FileNameExtensionFilter filter = new FileNameExtensionFilter("CSV Files", "csv");
    fileChooser.setFileFilter(filter);
    
    int result = fileChooser.showSaveDialog(this);
    
    if (result == JFileChooser.APPROVE_OPTION) {
        // Dapatkan path dan nama file yang dipilih
        File file = fileChooser.getSelectedFile();
        
        // Pastikan file berformat .csv
        if (!file.getName().endsWith(".csv")) {
            file = new File(file.getAbsolutePath() + ".csv");
        }
        
        // Panggil metode untuk ekspor data ke CSV
        exportToCSV(file);
    }
    }//GEN-LAST:event_btnExportActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // Mendapatkan baris yang dipilih pada tabel
    int selectedRow = jTable1.getSelectedRow();

    // Jika tidak ada baris yang dipilih
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Silakan pilih barang yang akan diubah.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Ambil data yang ada pada baris yang dipilih
    String nama = txtNama.getText();
    String kategori = cmbKategori.getSelectedItem().toString();
    String jumlah = txtJumlah.getText();

    // Validasi input
    if (nama.isEmpty() || jumlah.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        // Pastikan jumlah adalah angka
        int jumlahInt = Integer.parseInt(jumlah);

        // Perbarui data pada tabel
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setValueAt(nama, selectedRow, 0);     // Update Nama Barang
        model.setValueAt(kategori, selectedRow, 1);  // Update Kategori
        model.setValueAt(jumlahInt, selectedRow, 2); // Update Jumlah

        // Bersihkan input field setelah data diubah
        clearFields();
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Jumlah harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnUbahActionPerformed

     private void clearFields() {
        txtNama.setText("");
        txtJumlah.setText("");
        cmbKategori.setSelectedIndex(0);  // Set kategori default
    }
    

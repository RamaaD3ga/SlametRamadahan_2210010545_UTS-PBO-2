    public void exportToCSV(File file) {
    try (BufferedWriter writer = new BufferedWriter(new FileWriter(file))) {
        // Tuliskan header (opsional)
        writer.write("Kolom1,Kolom2,Kolom3");
        writer.newLine();
        
        // Tuliskan data (contoh data)
        writer.write("Data1,Data2,Data3");
        writer.newLine();
        
        writer.write("Data4,Data5,Data6");
        writer.newLine();

        // Konfirmasi penyimpanan berhasil
        System.out.println("File berhasil disimpan: " + file.getAbsolutePath());
    } catch (IOException e) {
        e.printStackTrace();
        System.out.println("Terjadi kesalahan saat menyimpan file.");
    }
}
